#import "YYMineOnlineChatViewController.h"

@interface YYMineOnlineChatViewController ()<UIWebViewDelegate>

/** webview*/
@property (nonatomic, strong) UIWebView *webView;

@end

@implementation YYMineOnlineChatViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    self.webView = [[UIWebView alloc] initWithFrame:self.view.bounds];
    [self.view addSubview:self.webView];
    self.webView.delegate = self;
    [SVProgressHUD showWithStatus:@"正在加载..."];
    NSURL *url = [NSURL URLWithString:@"http://yyapp.1yuaninfo.com/yyfwapp/kefu.html"];
    NSURLRequest *request = [NSURLRequest requestWithURL:url];
    [self.webView loadRequest:request];
}

- (void)webViewDidFinishLoad:(UIWebView *)webView {
    [SVProgressHUD dismiss];
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

